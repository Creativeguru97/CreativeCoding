class P4Vector {

  constructor(x, y, z, w) {
    this.x = x;
    this.y = y;
    this.z = z;
    this.w = w;
  }
}


class P5Vector{

  constructor(x, y, z, w, u){
    this.x = x;
    this.y = y;
    this.z = z;
    this.w = w;
    this.u = u;
  }
}


class P6Vector{

  constructor(x, y, z, w, u, t){
    this.x = x;
    this.y = y;
    this.z = z;
    this.w = w;
    this.u = u;
    this.t = t;
  }
}
